package itesm.business;

public class ConsultaBean {
	
	private String id_consulta;
	private String fecha_consulta;
	private String hora_inicial;
	private String hora_final;
	private String id_empleado;
	private String id_sala;
	private String id_paciente;
	private String correo;
	private String observaciones;
	private String estado_consulta;
	private String id_atencion;
	
	public String getId_consulta() {
		return id_consulta;
	}
	public void setId_consulta(String id_consulta) {
		this.id_consulta = id_consulta;
	}
	public String getFecha_consulta() {
		return fecha_consulta;
	}
	public void setFecha_consulta(String fecha_consulta) {
		this.fecha_consulta = fecha_consulta;
	}
	public String getHora_inicial() {
		return hora_inicial;
	}
	public void setHora_inicial(String hora_inicial) {
		this.hora_inicial = hora_inicial;
	}
	public String getHora_final() {
		return hora_final;
	}
	public void setHora_final(String hora_final) {
		this.hora_final = hora_final;
	}
	public String getId_empleado() {
		return id_empleado;
	}
	public void setId_empleado(String id_empleado) {
		this.id_empleado = id_empleado;
	}
	public String getId_sala() {
		return id_sala;
	}
	public void setId_sala(String id_sala) {
		this.id_sala = id_sala;
	}
	public String getId_paciente() {
		return id_paciente;
	}
	public void setId_paciente(String id_paciente) {
		this.id_paciente = id_paciente;
	}
	public String getCorreo() {
		return correo;
	}
	public void setCorreo(String correo) {
		this.correo = correo;
	}
	public String getObservaciones() {
		return observaciones;
	}
	public void setObservaciones(String observaciones) {
		this.observaciones = observaciones;
	}
	public String getEstado_consulta() {
		return estado_consulta;
	}
	public void setEstado_consulta(String estado_consulta) {
		this.estado_consulta = estado_consulta;
	}
	public String getId_atencion() {
		return id_atencion;
	}
	public void setId_atencion(String id_atencion) {
		this.id_atencion = id_atencion;
	}
	
}
